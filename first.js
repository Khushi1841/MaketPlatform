// // Calculate the sum of numbers from 1 to 100
let sum = 0;
for (let i = 1; i <= 100; i++) {
    sum += i;
}

// Display the sum using an alert box
alert("The sum of numbers from 1 to 100 is: " + sum);

// Calculate the product of numbers from 1 to 50
let product = 1;
for (let i = 1; i <= 50; i++) {
    product *= i;
}

// Display the product using an alert box
alert("The product of numbers from 1 to 50 is: " + product);
